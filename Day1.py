# Print examples
print("Hello World!")
print(123)
print(1232.5)
print("Hello", "World")
print("Hello", 123, True)
print("Hello", "World", 123, sep="#")


# End parameter in print
print("Hello", end="\n")
print("World!", end="\n")
print(123)

# Complex number
print(5 + 6j)

# List
print([1, 2, "h"])

# Tuple
print((544, 'dfgdf', True))

# Set
print({"type", 2324, False})

# Dictionary
print({"Name": "Abdullah", "Roll No": 123})

# String examples
print('My name is Abdullah')
print("My name is" + " " + "Abdullah")
print('''Abdullah''')

# Type checking
print(type(123))
print(type(True))
print(type([1, 2, 3, 5]))